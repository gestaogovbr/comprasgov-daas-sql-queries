--	max(a119.CO_ORGAO_MAXI)  CO_ORGAO_MAXI,
	max(a119.NO_ORGAO_MAXI)  EmitenteOrgaoMaximo,
--	a18.ID_UG  ID_UG,
--	max(a112.CO_UG)  CO_UG,
	max(a112.NO_UG)  EmitenteUG,
	a13.ID_DOC_HABIL  DocumentoHabil,
	a16.VA_DOC  DHValor,
--	a11.ID_DIA  ID_DIA_ATESTE_DOC_HABIL,
