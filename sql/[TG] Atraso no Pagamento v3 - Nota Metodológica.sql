	max(lpad(a16.ID_ANO_ATESTE_DOC_HABIL,4,2)||'-'||lpad(a16.ID_MES_ATESTE_DOC_HABIL,2,0)||'-'||lpad(a16.ID_DIA_ATESTE_DOC_HABIL,2,0)) DHDataAteste,
	a15.ID_DOCUMENTO  ID_DOCUMENTO,
--	a15.ID_ANO_EMISSAO_DOC  ID_ANO_EMISSAO_DOC,
--	a17.ID_ANO_EMISSAO_DOC  ID_ANO,
--	a17.ID_MES_EMISSAO_DOC  ID_MES,
--	a17.ID_DIA_EMISSAO_DOC  ID_DIA,
