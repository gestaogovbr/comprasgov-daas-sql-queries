	max(lpad(a11.ID_ANO,4,2)||'-'||lpad(a11.ID_MES,2,0)||'-'||lpad(a11.ID_DIA,2,0)) DHDataEmissaoDocOrigem,
--	a16.ID_DIA_ATESTE_DOC_HABIL  ID_DIA_ATESTE_DOC_HABIL0,
--	a16.ID_MES_ATESTE_DOC_HABIL  ID_MES_ATESTE_DOC_HABIL0,
